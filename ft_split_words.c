#include "libft.h"

char	**ft_split_words(const char *s, char c, char **tab)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	k = 0;
	while (s[i] != '\0')
	{
		j = 0;
		while (s[i] != '\0' && s[i] == c)
			i++;
		while (s[i] != '\0' && s[i] != c)
			tab[k][j++] = s[i++];
		k++;
	}
	return (tab);
}
