#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	r;
	int	sign;

	r = 0;
	i = 0;
	sign = 1;
	while (str[i] <= 32)
		i++;
	if (str[i] == '+')
		i++;
	if (str[i] == '-')
	{
		sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + str[i] - '0';
		i++;
	}
	return (sign * r);
}
