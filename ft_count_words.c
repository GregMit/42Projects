#include "libft.h"

int		ft_count_words(const char *s, char c)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (s[i] != '\0')
	{
		if ((s[i] != c && s[i + 1] == c) || (s[i] != c && s[i + 1] == '\0'))
			k++;
		i++;
	}
	return (k + 1);
}
